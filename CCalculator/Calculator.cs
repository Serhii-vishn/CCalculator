namespace CCalculator
{
    public static class Calculator
    {
        public static  double Plus(double frstOperand, double scndOperand)
        {
            return frstOperand + scndOperand;
        }

        public static double Minus(double frstOperand, double scndOperand)
        {
            return frstOperand - scndOperand;
        }

        public static double Multiply(double frstOperand, double scndOperand)
        {
            return frstOperand * scndOperand;
        }

        public static double Divide(double frstOperand, double scndOperand)
        {
            return frstOperand / scndOperand;
        }
    }
}